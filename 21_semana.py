# 21. Escribir un programa que dado un número del 1 a 7 escriba el correspondiente nombre del
# día de la semana.

dia = int(input("Digite el numero del 1 al 7:\n"))
if dia == 1:
    print("Lunes")
elif dia == 2:
    print("Martes")
elif dia == 3:
    print("Miercoles")
elif dia == 4:
    print("Jueves")
elif dia == 5:
    print("Viernes")
elif dia == 6:
    print("Sabado")
elif dia == 7:
    print("Domingo")
