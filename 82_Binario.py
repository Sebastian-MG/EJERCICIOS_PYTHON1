# Crear un módulo de funciones aritméticas que realicen las operaciones de suma, resta, multiplicación, división y potencia de enteros, escribir un programa que importe este módulo y use estas funciones para operar con números capturados desde el teclado.
def sum(a,b):
    return int(a,2) + int(b,2)
def resta(a,b):
    return int(a,2) - int(b,2)
def div(a,b):
    return int(a,2)/int(b,2)
def multi(a,b):
    return int(a,2)*int(b,2)
